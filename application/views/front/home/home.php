                <br>
                <p>Sistem Informasi Unit Pengelola Benih Sumber (SI-UPBS) merupakan sistem informasi yang pada awalnya dikembangkan untuk mempermudah sistem monitoring dan pelaporan terkait dengan kegiatan produksi, distribusi, stok dan manajemen benih yang dilaksanakan oleh UPBS di BPTP/LPTP. UPBS BPTP/LPTP adalah lembaga produksi benih di bawah manajemen Balai/Loka Pengkajian Teknologi Pertanian (BPTP/LPTP) yang ada di setiap provinsi di seluruh Indonesia dan secara resmi berjalan serentak pada tahun 2012. Keberadaan UPBS pada tahap awal adalah untuk mendukung swasembada pangan khususnya padi, jagung, dan kedelai berkelanjutan.</p>
                <p>Peran utama UPBS BPTP/LPTP adalah melakukan produksi benih kelas FS (benih dasar), SS (benih pokok), dan ES (benih sebar). Varietas benih padi yang dihasilkan oleh UPBS BPTP/LPTP merupakan varietas-varietas yang telah resmi dilepas, namun masih belum banyak dikenal oleh masyarakat secara luas seperti berbagai varietas Inpari, Inpara dan Inpago. Produksi benih padi varietas-varietas terbaru yang dilakukan UPBS ditujukan untuk mendukung diseminasi inovasi pertanian agar petani dapat memperoleh benih berkualitas prima dengan varietas yang lebih unggul dibandingkan dengan yang telah digunakan petani selama ini.</p>
